    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int baris = table.getSelectedRow();
        if(baris!= -1){
            int pilih = JOptionPane.showConfirmDialog(this, "Apakah anda yakin ingin menghapusnya?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if(pilih ==0){
                try {
                    Connection c = Koneksi.MySQL();
                    Statement st = c.createStatement();
                    Object oid = table.getValueAt(baris, 0);
                    String strid = oid.toString();
                    int id = Integer.parseInt(strid);
                    String q = "DELETE FROM data WHERE id_data="+ id +"";
                    st.executeUpdate(q);
                    loadData("");
                    JOptionPane.showMessageDialog(this, "Berhasil");
                } catch (HeadlessException| NumberFormatException | SQLException e) {
                }
                    
                
                
            }else {
                table.clearSelection();
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        int baris = table.getSelectedRow();
        if(baris != -1){
            int id = Integer.parseInt(table.getValueAt(baris, 0).toString());
            String kode = table.getValueAt(baris, 1).toString();
            String nama = table.getValueAt(baris, 2).toString();
            String kategori = table.getValueAt(baris, 3).toString();
            String lokasi = table.getValueAt(baris, 4).toString();
            String deskripsi = table.getValueAt(baris, 5).toString();
            String tanggal = table.getValueAt(baris, 6).toString();
            Dokumen dok = new Dokumen();
            dok.setId(id);
            dok.setKode(kode);
            dok.setKategori(kategori);
            dok.setDeskripsi(deskripsi);
            dok.setNama(nama);
            dok.setLokasi(lokasi);
            dok.setTanggal(tanggal);
            
            Home.panggilPanel(new Ubah(dok));
        }
    }//GEN-LAST:event_btnUbahActionPerformed

