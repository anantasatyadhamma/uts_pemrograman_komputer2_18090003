    private void loadData(String key){
        try {
            model.setRowCount(0);
            String where = !key.isEmpty() ? "WHERE kode LIKE '%"+key+"%' OR nama LIKE '%"+key+"%' OR kategori LIKE '%"+key+"%' OR lokasi LIKE '%"+key+"%' OR deskripsi LIKE '%"+key+"%' OR tanggal LIKE '%"+key+"%'" : "";
            String q = "SELECT * FROM data "+where;
            Connection c = Koneksi.MySQL();
            Statement st = c.createStatement();
            ResultSet rs = st.executeQuery(q);
            int nomor =0;
            while(rs.next()){
                int id = rs.getInt("id_data");
                String kode = rs.getString("kode");
                String nama = rs.getString("nama");
                String kategori = rs.getString("kategori");
                String lokasi = rs.getString("lokasi");
                String tanggal = rs.getString("tanggal");
                String deskripsi = rs.getString("deskripsi");
                nomor++;
                Object[] data = {id, nomor, kode, nama, kategori, lokasi, deskripsi, tanggal };
                model.addRow(data);
            }
        } catch (Exception e) {
        }
    }

